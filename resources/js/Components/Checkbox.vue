<script setup>
import { computed } from 'vue';

const emit = defineEmits(['update:checked']);

const props = defineProps({
    checked: {
        type: [Array, Boolean],
        required: true,
    },
    value: {
        default: null,
    },
});

const proxyChecked = computed({
    get() {
        return props.checked;
    },

    set(val) {
        emit('update:checked', val);
    },
});
</script>

<template>
    <input
        type="checkbox"
        :value="value"
        v-model="proxyChecked"
        class="rounded dark:bg-neutral-900 border-neutral-300 dark:border-neutral-700 text-accent-600 shadow-sm focus:ring-accent-500 dark:focus:ring-accent-600 dark:focus:ring-offset-neutral-800"
    />
</template>
